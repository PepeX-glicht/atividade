
// Validador de Dias
// Estrutura de controle que mapeia números para nomes de dias da semana.

export function exercicio_swicht() {

let dia = 1;

switch (dia) {
  
    case 1:
        console.log("Segunda");
        break;
    case 2:
        console.log("Terça");
        break;
    case 3:
        console.log("quarta");
        break;
    case 4:
        console.log("quinta");
        break;
    default:
        console.log("Outro dia");
    }
}
