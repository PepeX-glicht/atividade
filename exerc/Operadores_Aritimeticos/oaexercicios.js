function exercicio1() {
    let a = 10;
    let b = 5;

    console.log(a + b); // soma
    console.log(a - b); // subtração
    console.log(a * b); // multiplicação
    console.log(a / b); // divisão
    console.log(a % b); // resto
    console.log(Math.pow(a, b)); // potência
    console.log(Math.sqrt(a)); // raiz
}

exercicio1();